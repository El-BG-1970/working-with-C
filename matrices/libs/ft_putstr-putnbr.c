#include <stdio.h>

void ft_putstr(char *str)
{
  int pos;

  pos = 0;
  while(str[pos])
    putchar(str[pos++]);
}

void ft_putnbr(int nb)
{
  if (nb == -2147483648)
    {
      ft_putstr("-214748364");
      nb = 8;
    }
  if (nb < 0)
    {
      nb = -nb;
      putchar('-');
    }
  if (nb > 9)
    {
      ft_putnbr(nb / 10);
      ft_putnbr(nb % 10);
    }
  else
    putchar(nb + '0');
}

void ft_putlst(int *lst)
{
  int pos;
  
  pos = 0;
  while(lst[pos])
    ft_putnbr(lst[pos++]);
}
